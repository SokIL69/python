    content = f.read()
    print(f"Содержимое файла: \n{content}")
    # Определяем количество байт в файле.
    bites = f.tell()

    # Определяем количество строк в файле, количество слов в строке, количество слов в файле.
    # Слово - набор символов без пробелов.
