    lines = 0
    words = 0
    for line in f:
        lines += 1
        words_in_line = 0
        pos = 'out'
        for letter in line:
            if letter != ' ' and pos == 'out':
                words += 1
                words_in_line += 1
                pos = 'in'
            elif letter == ' ':
                pos = 'out'
        print(f"Количество слов в строке {lines}: {words_in_line}")

    print(f"Количество строк в файле: {lines}")
    print(f"Количество слов в файле: {words}")
    print(f"Количество байт в файле: {bites}")
